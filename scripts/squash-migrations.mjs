// Concatenates schema.sql + all supabase/migrations/*.sql in chronological order
// into a single supabase/schema_full.sql for faster CI/staging setup while keeping
// individual migration files for traceability (ROADMAP.md P2-8).
import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = path.dirname(path.dirname(fileURLToPath(import.meta.url)));

const files = [
  path.join("supabase", "schema.sql"),
  ...readdirSync(path.join(rootDir, "supabase", "migrations"))
    .filter((f) => f.endsWith(".sql"))
    .sort()
    .map((f) => path.join("supabase", "migrations", f)),
];

const output = [
  "-- THIS FILE IS AUTO-GENERATED by scripts/squash-migrations.mjs",
  `-- Last updated: ${new Date().toISOString()}`,
  "-- Do not edit directly. Keep individual migration files and regenerate this.",
  "",
];

for (const file of files) {
  const content = readFileSync(path.join(rootDir, file), "utf8");
  output.push(`-- BEGIN ${file}`, "", content, "", `-- END ${file}`, "");
}

const outputPath = path.join(rootDir, "supabase", "schema_full.sql");
writeFileSync(outputPath, output.join("\n"));
console.log(`Generated ${outputPath} (${files.length} files)`);
